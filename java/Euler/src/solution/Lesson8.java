package solution;

public class Lesson8 {
	private int x;
	private int y;

public Lesson8(int X, int Y) {
	this.setX(X);
	this.setY(Y);
	}

	public int getX() {
		return this.x;
	}

	private void setX(int px) {
		x = px;
	}

	public int getY() {
		return this.y;
	}

	private void setY(int py) {
		y = py;
	}
}
